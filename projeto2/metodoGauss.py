def eliminacao(A, B):
	n = len(A)

	for k in range (0, n):
		for i in range(k + 1, n):
			m = A[i][k] / A[k][k]
			A[i][k] = 0

			for j in range (k + 1, n):
				A[i][j] = round(A[i][j] - m * A[k][j], 10)

			B[i] = B[i] - m * B[k]


def resolucaoSistema(A, B):
	x = [0 for i in range(0, len(A))]
	n = len(A) - 1

	x[n] = B[n] / A[n][n]

	for i in range(n - 1, -1, -1):
		soma = 0

		for j in range (i + 1, n):
			soma = soma + A[i][j] * x[j]

		x[i] = (B[i] - soma) / A[i][i]

	return x

